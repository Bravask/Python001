'''
11.	Faça um Script em Python que solicite a largura e a altura de um retângulo. Em seguida, imprima para o usuário quantos metros quadrados possui a área total do quadrado. 
'''

alt_retan = float(input("Digite a altura do seu retângulo: "));
larg_retan= float(input("Digite a largura do seu retângulo: "));
metros_quad = alt_retan * larg_retan;
print(f"A área total de metros quadrados do seu retângulo é de: {metros_quad}");
